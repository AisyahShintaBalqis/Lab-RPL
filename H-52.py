List = ['A', 'I', 'S', 'Y', 'A', 'H',
        'S', 'H', 'I', 'N', 'T', 'A',
        'B', 'A', 'L', 'Q', 'I', 'S']
print("Initial List: ")
print(List)
 

Sliced_List = List[6:12]
print("\nIrisan Elemen list dari index 6-12: ")
print(Sliced_List)
 

Sliced_List = List[12:]
print("\nIrisan elemen dari index 12 "
      "sampai terakhir: ")
print(Sliced_List)
 

Sliced_List = List[:]
print("\nPrint semua Elemen dengan operator Slice: ")
print(Sliced_List)
