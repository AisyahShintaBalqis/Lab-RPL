List = ['A', 'I', 'S', 'Y', 'A', 'H',
        'S', 'H', 'I', 'N', 'T', 'A',
        'B', 'A', 'L', 'Q', 'I', 'S']
print("Initial List: ")
print(List)
 

Sliced_List = List[:-6]
print("\nSlice Element index negatif 6 sampai terakhir: ")
print(Sliced_List)
 

Sliced_List = List[-6:18]
print("\nSlice Element dari index -6 ke 18")
print(Sliced_List)
 

Sliced_List = List[::-1]
print("\nElement List di cetak terbalik: ")
print(Sliced_List)
