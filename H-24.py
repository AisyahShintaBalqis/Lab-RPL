angka_pertama = int(input("Angka pertama : "))
angka_kedua = int(input("Angka kedua : "))
operator =(input("Operator : "))

if operator == '+' :
    hasil = angka_pertama + angka_kedua
    print("Hasil : ",hasil)

elif operator == '-' :
    hasil = angka_pertama - angka_kedua
    print("Hasil : ",hasil)

elif operator == '/' :
    hasil = angka_pertama / angka_kedua
    print("Hasil : ",hasil)

elif operator == 'x' :
    hasil = angka_pertama * angka_kedua
    print("Hasil : ",hasil)

else :
    ("Maaf, Operator yang anda input tidak tersedia !!!")

    
                    
    
